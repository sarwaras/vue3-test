<template>
  <div>
    <h2>APP 父级组件</h2>
    <button @click="isShow = !isShow">切换显示</button>
    <hr />
    <ChildS v-if="isShow" />
  </div>
</template>

<script lang="ts">
import { defineComponent, ref } from "vue"
import ChildS from "./components/ChildS.vue"

export default defineComponent({
  name: "App",
  components: { ChildS },

  setup() {
    const isShow = ref(true)
    return {
      isShow,
    }
  },
})
</script>
